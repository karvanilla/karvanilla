function gcd(a, b) {
    // Проверяем, что оба числа неотрицательные
    if (a < 0 || b < 0) {
        throw new Error("Оба числа должны быть неотрицательными.");
    }

    // Алгоритм Евклида
    while (b !== 0) {
        let temp = b;
        b = a % b; // Остаток от деления
        a = temp;  // Обновляем a
    }

    return a; // Возвращаем делитель
}

// Примеры использования
console.log(gcd(2442, 52432)); 
console.log(gcd(22, 44)); 
console.log(gcd(354238645, 234618)); 